export interface OllamaModel {
  name: string;
  sizeBytes: number;
  paramSize?: string | null;
  quantization?: string | null;
  family?: string | null;
  /** Ollama's real capability tags ("completion", "vision", "image",
   *  "thinking", …). */
  capabilities?: string[] | null;
}

export interface ProviderModel {
  id: string;
  name?: string | null;
  modelType?: string | null;
  tier?: string | null;
}

/** A selectable chat model with its serving connection resolved.
 *  Picker keys: "eaon:<id>", "freeTrial:<id>", "ollama:<tag>", "custom:<configId>:<modelId>". */
export interface ModelEntry {
  key: string;
  /** The id sent in the request "model" field. */
  requestId: string;
  display: string;
  provider:
    | { kind: "eaon" }
    /** Same hosted catalog as "eaon", routed through the Free Week trial
     *  credential instead of the user's own key — its own provider, always
     *  selectable regardless of whether an Eaon API key is set. */
    | { kind: "freeTrial" }
    | { kind: "ollama" }
    | { kind: "custom"; configId: string; configName: string };
  tier?: string | null;
  supportsVision?: boolean;
}

export type SidebarSelection =
  | { kind: "chat" }
  | { kind: "projects" }
  | { kind: "models" }
  | { kind: "project"; id: string };

export interface PullState {
  status: string;
  completed: number;
  total: number;
  error?: string;
}

export interface SystemSpecs {
  totalMemBytes: number;
  cpuCores: number;
  os: string;
  arch: string;
}

/** Hardware-fit verdict for a model download, from RAM math alone. */
export type FitVerdict = "fits" | "tight" | "too-big" | "unknown";
